//Literales octales y binarios
//
// Hay ocasiones en que el contexto de nuestros datos requere que
// trabajemos con cifras no decimales, por ejemplo en base 2 (binario) o base
// 8 (octal), ahora es sencillo crear este tipo de literales con los prefijos (0b) y
// (0o) respectivamente.

var b = 0b111110111; // Binario
console.log( b );     // 503

var o = 0o767;    // Octal
console.log( o ); // 503